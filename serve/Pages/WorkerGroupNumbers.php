<div class="box" style="margin-top: 30px;">

    <h4>Group 1:</h4> <h5>Senior management in large business organisation, government administration and defence, and qualified professionals</h5>
    <ul>
      <li> <strong>Senior executive/manager/department head in industry, commerce, media or other large organisation.</strong> </li>
      <li> <strong>Public service manager</strong> (Section head or above), regional director, health/education/police/fire services administrator </li>
      <li> <strong>Other administrator</strong> [school principal, faculty head/dean, library/museum/gallery director, research facility director] </li>
      <li> <strong>Defence Forces</strong> Commissioned Officer </li>
      <li> <strong>Professionals</strong> generally have degree or higher qualifications and experience in applying this knowledge to design, develop or operate complex systems; identify, treat and advise on problems; and teach others. </li>
      <li> <strong>Health, Education, Law, Social Welfare, Engineering, Science, Computing</strong> professional </li>
      <li> <strong>Business</strong> [management consultant, business analyst, accountant, auditor, policy analyst, actuary, valuer] </li>
      <li> <strong>Air/sea transport</strong> [aircraft/ship’s captain/officer/pilot, flight officer, flying instructor, air traffic controller] </li>
    </ul>

    <hr class="divider" />

    <h4>Group 2:</h4> <h5>Other business managers, arts/media/sportspersons and associate professionals</h5>
    <ul>
      <li> <strong>Owner/manager</strong> of farm, construction, import/export, wholesale, manufacturing, transport, real estate business </li>
      <li> <strong>Specialist manager</strong> [finance/engineering/production/personnel/industrial relations/sales/marketing] </li>
      <li> <strong>Financial services manager</strong> [bank branch manager, finance/investment/insurance broker, credit/loans officer] </li>
      <li> <strong>Retail sales/services manager</strong> [shop, petrol station, restaurant, club, hotel/motel, cinema, theatre, agency] </li>
      <li> <strong>Arts/media/sports</strong> [musician, actor, dancer, painter, potter, sculptor, journalist, author, media presenter, photographer, designer, illustrator, proof reader, sportsman/woman, coach, trainer, sports official] </li>
      <li> <strong>Associate professionals</strong> generally have diploma/technical qualifications and support managers and professionals. </li>
      <li> <strong>Health, Education, Law, Social Welfare, Engineering, Science, Computing</strong> technician/associate professional </li>
      <li> <strong>Business/administration</strong> [recruitment/employment/industrial relations/training officer, marketing/advertising specialist, market research analyst, technical sales representative, retail buyer, office/project manager] </li>
      <li> <strong>Defence Forces</strong> senior Non-Commissioned Officer </li>
    </ul>

    <hr class="divider" />

    <h4>Group 3:</h4> <h5>Tradesmen/women, clerks and skilled office, sales and service staff</h5>
    <ul>
      <li> <strong>Tradesmen/women</strong> generally have completed a 4 year Trade Certificate, usually by apprenticeship. All tradesmen/women are included in this group. </li>
      <li> <strong>Clerks</strong> [bookkeeper, bank/PO clerk, statistical/actuarial clerk, accounting/claims/audit clerk, payroll clerk,
  recording/registry/filing clerk, betting clerk, stores/inventory clerk, purchasing/order clerk,
  freight/ transport/shipping clerk, bond clerk, customs agent, customer services clerk, admissions clerk]
  Skilled office, sales and service staff.</li>
      <li> <strong>Office</strong> [secretary, personal assistant, desktop publishing operator, switchboard operator] </li>
      <li> <strong>Sales</strong> [company sales representative, auctioneer, insurance agent/assessor/loss adjuster, market researcher] </li>
      <li> <strong>Service</strong> [aged/disabled/refuge/child care worker, nanny, meter reader, parking inspector, postal worker, courier,
  travel agent, tour guide, flight attendant, fitness instructor, casino dealer/supervisor] </li>
    </ul>

    <hr class="divider" />

    <h4>Group 4:</h4> <h5>Machine operators, hospitality staff, assistants, labourers and related workers</h5>
    <ul>
      <li> <strong>Drivers, mobile plant, production/processing machinery and other machinery operators.</strong> </li>
      <li> <strong>Hospitality staff</strong> [hotel service supervisor, receptionist, waiter, bar attendant, kitchenhand, porter, housekeeper] </li>
      <li> <strong>Office assistants, sales assistants and other assistants.</strong> </li>
      <li> <strong>Office</strong> [typist, word processing/data entry/business machine operator, receptionist, office assistant] </li>
      <li> <strong>Sales</strong> [sales assistant, motor vehicle/caravan/parts salesperson, checkout operator, cashier, bus/train conductor,
  ticket seller, service station attendant, car rental desk staff, street vendor, telemarketer, shelf stacker] </li>
      <li> <strong>Assistant/aide</strong> [trades’ assistant, school/teacher's aide, dental assistant, veterinary nurse, nursing assistant,
  museum/gallery attendant, usher, home helper, salon assistant, animal attendant] </li>
      <li> <strong>Labourers and related workers</strong> </li>
      <li> <strong>Defence Forces</strong> ranks below senior NCO not included above </li>
      <li> <strong>Agriculture, horticulture, forestry, fishing, mining worker</strong> [farm overseer, shearer, wool/hide classer, farmhand, horse trainer, nursery/green keeper, gardener, tree surgeon, forestry/logging worker, miner, seafarer/fishing hand] </li>
      <li> <strong>Other worker</strong> [labourer, factory hand, storeman, guard, cleaner, caretaker, laundry worker, trolley collector, car park attendant, crossing supervisor] </li>
    </ul>

</div>

